package day11_controlFlowStatements_5;

public class task47_Pattern {

	public static void main(String[] args) {
		
			
		int rows=6;
		int columns=6;
		
		for(int i=1;i<=rows;i++) {
			
			for(int j=1;j<=columns;j++) {
				
				System.out.print("*");
			
			}
			System.out.println();
			
			}
		}
			
			
			

	

}
