package week1;

public class PrintStatements {

	public static void main(String[] args) {
		
		System.out.println("Hello Every One, How Are You All Today?"); // appends new line at the end
		System.out.println("Hi");  // new line
		
		System.out.println("-------------------------------------");
		
		System.out.print("Hello Every One, How Are You All Today?"); // does not append new line
		System.out.print("Hi");

	}

}
